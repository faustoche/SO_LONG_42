#include "ft_printf.h"

int	ft_putun(unsigned int nb)
{
	int	len;

	len = 0;
	if (nb >= 10)
	{
		len += ft_putun(nb / 10);
	}
	len += ft_putc(nb % 10 + '0');
	return (len);
}
